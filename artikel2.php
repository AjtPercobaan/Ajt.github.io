                            <p class="details-para"> Cara dan Tips Merawat AC Agar Awet dan Tahan Lama</p>
                            <p>1. Bersihkan Filter AC Secara Berkala</p>
                            <p>Apabila AC di rumah Anda terasa tak sedingin biasanya, kemungkinan itu pertanda filter AC
                                sudah kotor. Guna mengatasinya, Anda perlu membersihkan filter tersebut secara berkala.

                                Anda dapat membuka bagian filter yang dirancang untuk lepas-pasang. Kemudian, cara
                                merawat AC adalah mencuci filter menggunakan air bersih dan sikat gigi bekas. </p>

                            <p>2. Pasang Termometer di dalam Ruangan</p>

                            <p>Cara merawat AC agar awet berikutnya adalah memasang termometer di dalam ruangan. Ini
                                bertujuan supaya Anda dapat memantau suhu udara dari AC dan suhu ruangan.

                                Apabila terjadi perbedaan, Anda bisa langsung tahu dan mengecek bagian mana yang menjadi
                                permasalahannya.</p>

                            <p>3. Aktifkan Fitur Timer,</p>

                            <p>Mengaktifkan fitur timer merupakan cara merawat AC rumah sendiri agar awet. Dengan
                                menggunakan fitur ini, AC rumah Anda akan mati secara otomatis sesuai waktu yang Anda
                                atur.

                                Selain awet, hal tersebut juga bisa membuat Anda jadi hemat listrik dan menghindari lupa
                                mematikan AC saat tidak diperlukan.

                                Sebaiknya, AC dibiarkan bekerja maksimal selama 5 jam dalam satu kali pemakaian. Jika
                                sudah melewati 5 jam, akan lebih baik bila AC diistirahatkan terlebih dahulu dengan
                                mematikannya.

                                Jadi, dengan adanya fitur timer Anda bisa mengatur AC untuk mati setiap 5 jam sekali
                                agar mesin kembali dingin.

                                Selain itu, hindari terlalu sering mengatur suhu AC di bawah 22 derajat Celcius karena
                                hal itu bisa membuat mesin AC cepat panas.</p>

                            <p>4. Cek Tekanan Freon</p>
                            <p>Cara merawat AC rumah berikutnya adalah selalu mengecek tekanan freon. Freon adalah bahan
                                yang digunakan untuk membuat udara menjadi dingin.

                                Jika udara dari AC Anda tidak dingin, berarti kemungkinan terjadi kebocoran freon yang
                                mengindikasikan tekanan freon menurun.

                                Untuk itu, periksalah tekanan freon secara rutin, misal 6 bulan sekali. Namun, sebaiknya
                                pengecekan ini dilakukan oleh orang yang ahli.</p>

                            <p>5. Periksa Termostat</p>
                            <p>Salah satu kesalahan umum yang sering dilakukan para pengguna AC adalah berkaitan dengan
                                pengaturan suhu termostat melalui remote.

                                Kebanyakan orang, mungkin termasuk Anda, menyalakan AC langsung pada pengaturan suhu
                                terendah dengan tujuan supaya cepat dingin. Padahal, itu justru berpengaruh pada
                                keawetan AC.

                                Jika AC terus dipaksa mencapai suhu terendah, maka kompresor harus bekerja secara
                                maksimum dan akhirnya dapat mengurangi umurnya. Alhasil AC menjadi cepat rusak.</p>

